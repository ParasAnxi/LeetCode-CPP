class Solution {
public:
  string reverseStr(string s, int k) {
    int st = 0;
    int e = min(k, (int)s.length());
    while (st < s.length()) {
      reverse(s.begin() + st, s.begin() + e);
      st += 2 * k;
      e = min(st + k, (int)s.length());
    }
    return s;
  }
};